package com.mb1.pemdas.programkrs;

public class Mahasiswa {

    private String nim;
    private String nama;

    public Mahasiswa(String nim, String nama) {
        this.nim = nim;
        this.nama = nama;
    }

    public String getNama() {
        return nama;
    }

    public String getNim() {
        return nim;
    }
    
}
